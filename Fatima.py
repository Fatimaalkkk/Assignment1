# Get input from the user for Departure Date and Time
def get_departure_date_time():
    while True:
        departure_date_time_str = input("Enter Departure Date and Time (YYYY-MM-DD HH:MM): ")
        if not departure_date_time_str:
            print("Error: Departure date and time are required. Please enter a valid date and time.")
        else:
            try:
                departure_date_time = datetime.strptime(departure_date_time_str, "%Y-%m-%d %H:%M")
                return departure_date_time
            except ValueError:
                print("Error: Invalid date/time format. Please enter the date/time in the format YYYY-MM-DD HH:MM.")


# Test the Departure Date and Time validation
