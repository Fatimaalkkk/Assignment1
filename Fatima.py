def validate_gate_number(gate_number):
    # Check if the gate number starts with "Gate-" and has a length greater than 5
    if gate_number.startswith("Gate-") and len(gate_number) > 5:
        return True
    else:
        return False

# Function to prompt user for gate number input and validate it
def enter_gate_number():
    gate_number = input("Enter the gate number for the boarding pass: ")
    if validate_gate_number(gate_number):
        print("Gate number is valid.")
    else:
        print("Invalid gate number entered.")

